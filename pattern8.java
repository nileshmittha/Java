public class pattern8 {



    public static void main(String[] args) {

        int co=1;
        

        for(int i=1; i<=5; i++){


            for(int j=1; j<=i; j++){

                System.out.print(co+" ");

                co=co+1;

                
            }

            System.out.println();
        }
    }
    
}
